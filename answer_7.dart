/** Answer to the question number 7:
  Question: What is method overriding?
  Answer:
         When a Child class redefines the method of its Parent class then it is called Method Overriding. 
         Overriding is done so that a child class can give its own implementation to the method which is already provided by the parent class.
 
  Question: How to create method overriding?
  Answer:
         The overriding method (the child class method) must be declared with the same configuration as the overridden method (the superclass method). 
         The return type, list of arguments and its sequence must be the same as the parent class method.
 */